# Operator Polymorphism
print("=== Operator Polymorphism Demo ===\n")

# Get numeric inputs
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

# Get string inputs
str1 = input("Enter first string: ")
str2 = input("Enter second string: ")

# Using + operator with numbers (addition)
print(f"\nNumeric Addition: {num1} + {num2} = {num1 + num2}")

# Using + operator with strings (concatenation)
print(f"String Concatenation: '{str1}' + '{str2}' = '{str1 + str2}'")
